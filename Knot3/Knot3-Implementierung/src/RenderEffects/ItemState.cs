/// <summary>
/// Der Zustand eines Menüeintrags.
/// </summary>
public enum ItemState
{
    /// <summary>
    /// Ausgewählt.
    /// </summary>
    Selected=1,
    /// <summary>
    /// Die Maus wurde direkt über den Menüeintrag navigiert und verweilt dort.
    /// </summary>
    Hovered=2,
    /// <summary>
    /// Kein gültiger oder ein undefinierter Zustand.
    /// Keine Reihenfolge.
    /// 
    /// </summary>
    None=0,
}

