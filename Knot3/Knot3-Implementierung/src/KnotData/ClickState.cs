/// <summary>
/// Eine Wertesammlung der möglichen Klickzustände einer Maustaste.
/// </summary>
public enum ClickState
{
    /// <summary>
    /// Kein gültiger oder ein undefinierter Zustand.
    /// Keine Reihenfolge.
    /// 
    /// </summary>
    None=0,
    /// <summary>
    /// Ein Einzelklick.
    /// </summary>
    SingleClick=1,
    /// <summary>
    /// Ein Doppelklick.
    /// </summary>
    DoubleClick=2,
}

